export function shuffle() {
    const size = PetImages.egg.length;
    let unshuffled = [];
    for (let i = 0; i < size; i += 1) {
        unshuffled.push(i);
    }
    const shuffled = unshuffled.map(value => ({value, sort: Math.random() }))
                                .sort((a, b) => a.sort - b.sort)
                                .map(({value}) => value)
    return shuffled;
}